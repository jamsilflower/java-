package com.ohgiraffers.변수연습용;

public class training {


    public static void main(String[] args) {

        //선언
        int sum = 20;
        int dum = 30;


        //초기화
        System.out.println("더하기 결과 : " + (sum + dum));
        System.out.println("빼기 결과 :  " + (sum - dum));
        System.out.println("곱하기 결과 : " + (sum * dum));
        System.out.println("나누기 결과 : " + sum / dum);
        System.out.println("나누기한 나머지 결과 " + (sum % dum));



    }
}
